turno = input('Digite o turno que você estuda: (M-matutino, V-Vespertino ou N- Noturno): ')

if(turno == 'M'):
    print('Bom dia princesa')
elif(turno == 'V'):
    print('Boa tarde, meu chapa')
elif(turno == 'N'):
    print('Boa noite que ela é uma criança, dalee')
else:
    print('Valor inválido! Digite direito!!!')